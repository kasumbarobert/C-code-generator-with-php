<h1>Rules for choosing variable names and function names</h1>
<h6>There are some restrictions on the names of variables and symbolic constants. -</h6>
<ol>
	<li>Names are made up of letters and digits;</li>
	<li>The first character must be a letter. The underscore ``_'' counts as a letter; it is sometimes useful for improving the readability of long variable names. </li>
	<li>Don't begin variable names with underscore, however, since library routines often use such names.</li>
	<li>Upper and lower case letters are distinct, so x and X are two different names.</li>
	<li>Variable names should be less than 31 characters</li>
	<li>Keywords like if, else, int, float, etc., are reserved: you can't use them as variable names. They must be in lower case. </li>
	<li>It's wise to choose variable names that are related to the purpose of the variable</li>
	<li>Arithmetic operators are not to be used in any names</li>
	<li></li>	
</ol>

